SELECT
--FROM RAW_ORDERS
    O.ORDERID
    , O.ORDERDATE
    , O.SHIPDATE
    , O.SHIPMODE
    , O.ORDERSELLINGPRICE
    , O.ORDERCOSTPRICE
--FROM RAW_CUSTOMER
    , C.CUSTOMERID
    , C.CUSTOMERNAME
    , C.SEGMENT
    , C.COUNTRY
--FROM RAW_PRODUCT
    , P.PRODUCTID
    , P.CATEGORY
    , P.PRODUCTNAME
    , P.SUBCATEGORY
    , (O.ORDERSELLINGPRICE - O.ORDERCOSTPRICE) AS ORDER_PROFIT
FROM
    {{ ref('RAW_ORDERS') }} AS O
LEFT JOIN
    {{ ref('RAW_CUSTOMER') }} AS C 
ON
    C.CUSTOMERID = O.CUSTOMERID
LEFT JOIN
    {{ ref('RAW_PRODUCT') }} AS P
ON
    P.PRODUCTID = O.PRODUCTID